def flag_to_country(flag):
    if len(flag) != 2:
        return None

    chars = []
    for c in flag:
        code = ord(c)
        if not (0x1F1E6 <= code <= 0x1F1FF):
            return None
        chars.append(chr(ord("A") + code - REGIONAL_BASE))

    return "".join(chars)

def language_from_flag(flag):
    country = flag_to_country(flag)
    if country is None:
        return None
    return COUNTRY_LANGUAGE.get(country)